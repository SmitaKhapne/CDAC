public class PrintDays
{
   public static void main(String[] args)
   {
      int day=6;

      switch(day)
      {
        case 1: System.out.println("Monday and It is weekday");
                break;
        case 2: System.out.println("Tuesday and It is weekday");
                break;
        case 3: System.out.println("Wednesday and It is weekday");
                break;
        case 4: System.out.println("Thusday and It is weekday");
                break;
        case 5: System.out.println("Friday and It is weekday");
                break;
        case 6: System.out.println("Saturday and It is weekend");
                break;
        case 7: System.out.println("Sunday and It is weekend");
                break;
      }
   }
}