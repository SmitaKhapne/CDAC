//m. Experiment with converting a double value into other primitive types or vice versa and observe the results

package Assignment_2;

public class Section7_M 
{
	public static void main(String[] args)
	{
			double b = 25.0;
			
			short s = (short)b;
			System.out.println("float:"+s);
			
			float f = (float)b;
			System.out.println("float:"+f);
			
			int n = (int)b;
			System.out.println("int:"+n);
			
			long l = (long)b;
			System.out.println("long:" +l);
			
			
			short num = 29;
			
			int fh = 25;
			
			long d1 = 56;
			
			System.out.println("short:" + num);
			System.out.println("int:" + fh);
			System.out.println("long:" + d1);
		}


}
