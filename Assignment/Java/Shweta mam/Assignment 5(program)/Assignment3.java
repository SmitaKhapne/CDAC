package shwetaMamAssignment;

public class Assignment3 
{
	public static void main(String[] args)
	{
		int a = 10;
		float f = 5.5f;
		double d = 10.000;
		double add = a + f + d;
		System.out.println("addition of different data type:" + add);
		
		double subtraction = a - f - d;
		System.out.println("subtraction of different data type:" + subtraction);
		
		double multiplication = a * f * d;
		System.out.println("multiplication of different data type:" + multiplication);
		
		double division = a / f / d;
		System.out.println("division of different data type:" + division);
		
	}

}
